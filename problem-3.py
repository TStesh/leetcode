"""
 Given a string s, find the length of the longest substring without repeating characters.
"""


def is_repeating(s: str) -> bool:
    d = {}
    for _ in s:
        if _ in d:
            return True
        d[_] = 1
    return False


def longest(s: str) -> int:
    # анализ по длине цепочки
    len_s = len(s)
    # цепочка пустая или из одного символа?
    if len_s < 2:
        return len_s
    # анализ на повторы
    set_s = set(s)
    len_set_s = len(set_s)
    # повтор одного символа?
    if len_set_s == 1:
        return 1
    # нет повторов?
    if len_set_s == len_s:
        return len_s
    # теперь у нас есть цепочка из более 1 неповторяющегося символа => длина макс. подцепочки >= 2 и <= len_s - 1
    # для поиска максимальной подстроки сначала смотрим подстроки длины len_s - 1, потом len_s - 2, ... , 3
    # когда проверили 3 и все с повторами, то смело возвращаем 2 (единцы быть не может)
    # цикл по длине подстроки
    for i in range(len_s - 1, 2, -1):
        # анализируем все подстроки длины i
        # выбираем подцепочки 0..(i-1), 1..i, ..., k..(i-1+k)
        # верхняя граница: len_s - 1 = Kmax + i - 1 => Kmax = len_s - i
        for j in range(len_s - i + 1):
            d = {}
            found = True
            for _ in s[j:(j + i)]:
                if _ in d:
                    found = False
                    break
                d[_] = 1
            if found:
                return i
    return 2


def longest2(s: str) -> int:
    len_s = len(s)
    if len_s < 2:
        return len_s
    set_s = set(s)
    len_set_s = len(set_s)
    if len_set_s == 1:
        return 1
    if len_set_s == len_s:
        return len_s
    longest_len = 0
    for (i, a) in enumerate(s[:-1]):
        counter = 1
        d = {a: 1}
        for _ in s[i + 1:]:
            counter += 1
            if _ in d:
                counter -= 1
                break
            else:
                d[_] = 1
        if longest_len < counter:
            longest_len = counter
        if longest_len == len_s:
            break
    return longest_len


